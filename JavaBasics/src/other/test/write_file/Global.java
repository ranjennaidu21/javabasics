package other.test.write_file;

public class Global {

	private String ProposalNumber;
	private int ProposalDate;
	private char DataSource;
	private boolean SubmissionType;
	
	public String getProposalNumber() {
		return ProposalNumber;
	}
	public void setProposalNumber(String proposalNumber) {
		ProposalNumber = proposalNumber;
	}
	public int getProposalDate() {
		return ProposalDate;
	}
	public void setProposalDate(int proposalDate) {
		ProposalDate = proposalDate;
	}
	public char getDataSource() {
		return DataSource;
	}
	public void setDataSource(char dataSource) {
		DataSource = dataSource;
	}
	public boolean isSubmissionType() {
		return SubmissionType;
	}
	public void setSubmissionType(boolean submissionType) {
		SubmissionType = submissionType;
	}
	

}
